#pragma once

#include <ostream>

#include "Data/temperature_sensors.hpp"

#include "Data/Struct.hpp"

struct Vec3 : public Struct {

  /////////////////////////////////////////////////////////////////////////////
  //                                ATTRIBURE                                //
  /////////////////////////////////////////////////////////////////////////////
  double x;
  double y;
  double z;

  /////////////////////////////////////////////////////////////////////////////
  //                               CONSTRUCTEUR                              //
  /////////////////////////////////////////////////////////////////////////////
  Vec3();

  Vec3(double p_x, double p_y, double p_z);

  /////////////////////////////////////////////////////////////////////////////
  //                               GET and SET                               //
  /////////////////////////////////////////////////////////////////////////////
  double get_x() const;

  void set_x(const double value);
  double get_y() const;

  void set_y(const double value);
  double get_z() const;

  void set_z(const double value);

  /////////////////////////////////////////////////////////////////////////////
  //                               FUNCTION                                  //
  /////////////////////////////////////////////////////////////////////////////

  virtual void to_stream(std::ostream &) const;
};

///////////////////////////////////////////////////////////////////////////////
//                               << STREAM >>                                //
///////////////////////////////////////////////////////////////////////////////
std::ostream &operator<<(std::ostream &os, const Vec3 &c);
std::istream &operator>>(std::istream &os, Vec3 &c);

std::ostream &operator<<(std::ostream &os, const Vec3 *c);
std::istream &operator>>(std::istream &os, Vec3 *&c);
