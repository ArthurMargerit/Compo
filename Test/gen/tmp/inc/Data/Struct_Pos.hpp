#pragma once

#include <ostream>

#include "Data/Types.hpp"

#include "Data/Struct.hpp"








struct Pos : public Struct {

  /////////////////////////////////////////////////////////////////////////////
  //                                ATTRIBURE                                //
  /////////////////////////////////////////////////////////////////////////////
  int x;
  int y;

  /////////////////////////////////////////////////////////////////////////////
  //                               CONSTRUCTEUR                              //
  /////////////////////////////////////////////////////////////////////////////
  Pos();

  

  Pos(int p_x,int p_y);
  

  /////////////////////////////////////////////////////////////////////////////
  //                               GET and SET                               //
  /////////////////////////////////////////////////////////////////////////////
  // x
  int get_x() const {
    return this->x;
  }

  void set_x(const int value) {
    this->x = value;
  }
  // y
  int get_y() const {
    return this->y;
  }

  void set_y(const int value) {
    this->y = value;
  }
};

///////////////////////////////////////////////////////////////////////////////
//                               << STREAM >>                                //
///////////////////////////////////////////////////////////////////////////////
std::ostream& operator<<(std::ostream& os, const Pos& c);
std::istream& operator>>(std::istream& os,Pos& c);
