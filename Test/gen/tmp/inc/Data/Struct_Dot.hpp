#pragma once

#include <ostream>

#include "Data/Types.hpp"

#include "Data/Struct.hpp"








struct Dot : public Struct {

  /////////////////////////////////////////////////////////////////////////////
  //                                ATTRIBURE                                //
  /////////////////////////////////////////////////////////////////////////////
  int h;
  int w;

  /////////////////////////////////////////////////////////////////////////////
  //                               CONSTRUCTEUR                              //
  /////////////////////////////////////////////////////////////////////////////
  Dot();

  

  Dot(int p_h,int p_w);
  

  /////////////////////////////////////////////////////////////////////////////
  //                               GET and SET                               //
  /////////////////////////////////////////////////////////////////////////////
  // h
  int get_h() const {
    return this->h;
  }

  void set_h(const int value) {
    this->h = value;
  }
  // w
  int get_w() const {
    return this->w;
  }

  void set_w(const int value) {
    this->w = value;
  }
};

